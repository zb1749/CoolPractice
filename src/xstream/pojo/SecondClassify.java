package xstream.pojo;


public class SecondClassify
{
    /**
     * 二级分类名
     */
    private String classifyKey;

    /**
     * 二级分类值
     */
    private String classifyValue;

    public String getClassifyKey()
    {
        return classifyKey;
    }

    public void setClassifyKey(String classifyKey)
    {
        this.classifyKey = classifyKey;
    }

    public String getClassifyValue()
    {
        return classifyValue;
    }

    public void setClassifyValue(String classifyValue)
    {
        this.classifyValue = classifyValue;
    }

}
