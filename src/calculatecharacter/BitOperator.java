package calculatecharacter;

public class BitOperator {
    public static void main(String[] args) {

        System.out.println("1<<10"+(1<<10));
        System.out.println("1<<20"+(1<<20));
        System.out.println("1<<30"+(1<<30));
        System.out.println("1<<40"+(1<<40));
        System.out.println("1<<50"+(1<<50));

        System.out.println("1>>>10"+(1>>>10));
        System.out.println("1>>>20"+(1>>>20));
        System.out.println("1>>>30"+(1>>>30));
        System.out.println("1>>>40"+(1>>>40));
        System.out.println("1>>>50"+(1>>>50));


        System.out.println("2>>>20"+(2>>>20));
        System.out.println("3>>>20"+(3>>>20));
        System.out.println("4>>>20"+(4>>>20));
        System.out.println("5>>>20"+(5>>>20));
        System.out.println("6>>>20"+(6>>>20));
        System.out.println("200>>>20"+(200>>>20));
    }
}
